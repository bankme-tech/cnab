/**
 * Interface que define as propriedades de um layout de registro base.
 */
export interface BaseRegistroLayout {
  /**
   * Campo de preenchimento com espaços em branco.
   * @example ' '
   */
  Branco107: string;

  /**
   * Campo de preenchimento com zeros à esquerda.
   * @example '000000000000'
   */
  Zeros162: string;

  /**
   * Campo de preenchimento com zeros à esquerda.
   * @example '0'
   */
  Zeros37: string;

  /**
   * Campo de preenchimento com zeros à esquerda.
   * @example '00000'
   */
  Zeros66: string;
}
